public class type_casting {
    public static void main(String[] args) {
        //wideing (Implicit)
    int n=10;
    System.out.println(n);
    double d=n;
    System.out.println(d);

        //Narrowing (explicit)
        double data=10.556;
        System.out.println(data);
        int data2=(int)data;
        System.out.println(data2);
    }


}
